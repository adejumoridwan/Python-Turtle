import turtle
def rectangle(color):
    t.begin_fill()
    t.fillcolor(color)
    for i in range(2):
        t.forward(400)
        t.right(90)
        t.forward(100)
        t.right(90)
    t.end_fill()

t=turtle.Turtle()
turtle.screensize(1100,1100)
turtle.title("National Flag Of Nigeria")
t.up()
t.pensize(4)
t.goto(0,-300)
t.down()
t.goto(0,400)
rectangle("Green")

t.goto(0,300)
t.forward(200)
t.color("black")
t.forward(200)
t.right(90)
t.forward(100)
t.right(90)
t.forward(400)
t.right(90)
t.forward(100)
t.right(90)
t.goto(0,200)
rectangle("green")

